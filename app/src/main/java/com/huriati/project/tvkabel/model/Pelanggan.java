package com.huriati.project.tvkabel.model;

public class Pelanggan {
    private String id, nama, jumlah_tagihan, wilayah_id, alamat, no_telp, no_ktp;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJumlah_tagihan() {
        return jumlah_tagihan;
    }

    public void setJumlah_tagihan(String jumlah_tagihan) {
        this.jumlah_tagihan = jumlah_tagihan;
    }

    public String getWilayah_id() {
        return wilayah_id;
    }

    public void setWilayah_id(String wilayah_id) {
        this.wilayah_id = wilayah_id;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNo_telp() {
        return no_telp;
    }

    public void setNo_telp(String no_telp) {
        this.no_telp = no_telp;
    }

    public String getNo_ktp() {
        return no_ktp;
    }

    public void setNo_ktp(String no_ktp) {
        this.no_ktp = no_ktp;
    }
}
